#!/bin/bash
export TBBROOT="/root/文档/tbb41_20130314oss" #
tbb_bin="/root/文档/tbb41_20130314oss/build/linux_intel64_gcc_cc4.6.1_libc2.13_kernel3.0.0_release" #
if [ -z "$CPATH" ]; then #
    export CPATH="${TBBROOT}/include" #
else #
    export CPATH="${TBBROOT}/include:$CPATH" #
